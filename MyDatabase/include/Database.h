// =============================================================================
// 文件:    Database.h
// 模块:    MeyerScan_Database.dll
// 版本号:  v1.3.0
//
// 用途:
//   数据库基础设施模块的公共接口头文件。提供统一的数据库访问接口，
//   支持 MySQL 和 SQLite 双数据库，实现连接管理、SQL 执行、事务管理、
//   数据库备份等核心功能。
//
// 架构角色:
//   本模块属于架构分层中的"数据/设备层"（第二层），作为通用数据库基础设施，
//   只供 DatabaseQtAdapter、领域服务、迁移工具、统计/导出等明确的数据访问边界执行 SQL。
//   Permission、UI 模块、MainExe 普通业务流程不得绕过服务层直接把 SQL 打到 Database。
//   本模块不依赖 Qt，连接/查询由纯 C++ 实现承担；Qt 模块需要 QString/QJson 便利能力时
//   应通过 MyDatabaseQtAdapter.dll 转换后再访问本模块。
//   业务逻辑统一在各模块的 Service 层处理。
//
// 接口规范:
//   所有方法均返回 Result<T> 或 VoidResult，调用方必须检查 IsSuccess()。
//   ErrorCode 和 Result<T>/VoidResult 当前由 Database 公共接口自行定义。
//   只有多个基础模块出现语义一致且稳定的结果合同时，才评估抽取最小公共头；
//   当前不把 Core.lib 作为 Database 的前置依赖。
//
// 日志规范:
//   本模块内部使用 MeyerScan_Logger.dll 输出结构化日志。
//   通过动态加载 Logger.dll 的方式引入，无需编译时链接 Logger.lib。
//
// 设计原则:
//   1. 接口与实现分离：通过纯虚类 IDatabase 定义接口，具体实现封装在 DLL 内部
//   2. 单一入口：通过 GetDatabase() 工厂函数获取唯一实例（单例模式）
//   3. 线程安全：所有操作均受互斥锁保护，支持多模块并发访问
//   4. 配置驱动：数据库连接参数通过 JSON 配置文件管理，便于部署和维护
//   5. 边界优先：Database 保持纯 C++，禁止把 Qt 类型、UI 或业务逻辑放入本模块
//
// 使用方法:
//   1. 包含此头文件并链接 MeyerScan_Database.lib
//   2. 调用 GetDatabase() 获取数据库接口实例
//   3. 调用 Init() 加载配置文件
//   4. 调用 Connect() 建立数据库连接
//   5. 使用 ExecuteQuery/ExecuteUpdate 执行 SQL
//   6. 使用完毕后调用 Disconnect() 和 Shutdown()
//
// 注意事项:
//   - MySQL 账号密码当前硬编码在 DLL 内部，后续会改为通过 ConfigCenter 加密存储
//   - 配置文件必须为有效的 JSON 格式
//   - 多线程环境下线程安全（通过 std::mutex 保护）
//   - 备份功能会复制整个数据库目录/文件，确保有足够的磁盘空间
//
// 依赖项:
//   - sqlite3.dll: SQLite 运行时（当前通过 LoadLibrary 动态加载）
//   - MeyerScan_Logger.dll: 结构化日志记录（可选依赖，通过动态加载引入）
//
// 作者:      MeyerScan Team
// 创建日期:  2026-06-16
// 最后修改:  2026-06-17
// =============================================================================

