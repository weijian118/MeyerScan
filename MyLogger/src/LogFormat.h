// =============================================================================
// 文件:    LogFormat.h
// 模块:    MeyerScan_Logger.dll（内部 — 不对外暴露）
//
// 用途:
//   将原始日志字段转换为一条固定格式的 UTF-8 行，准备写入磁盘。
//   所有格式决策（时间戳样式、字段宽度、空字段占位文本）
//   都集中在这个翻译单元中，这样可以在一个地方更改日志格式，
//   而无需触及任何调用方。
//
// 格式规范:
//   [2026-06-12 14:30:25.123] [INFO ] [CaseManager] [CreateCase] [Dev:SN001] [Case:C001] [Op:DrWang] text...
//   ^^^^^^^^^^^^^^^^^^^^^^^^  ^^^^^^  ^^^^^^^^^^^^^  ^^^^^^^^^^^^^ ^^^^^^^^^^^^ ^^^^^^^^^^^^ ^^^^^^^^^^ ^^^^^^
//   时间戳 (23 字符)          级别    模块           操作          设备ID        病例ID       操作员     内容
//                              5 字符  ≤31 字符       ≤31 字符      ≤8 字符       ≤16 字符     ≤16 字符   自由
//
//   - 时间戳使用 Windows SYSTEMTIME 结构体（本地时间，非 UTC）。
//     这是有意为之 —— 操作员和现场工程师在生成日志的机器上阅读这些日志。
//   - 调用方不得包含尾随换行符；LogWriter::WriteLine()
//     在行尾追加 '\n'，使每次系统级写入恰好是一条完整的日志条目。
//   - 空字段用 "-"（单个短横线）表示，以保持列在等宽文本编辑器中
//     视觉上对齐。
//   - 如果 content 字段极长，行将被截断到 2047 字节。
//     这是安全限制而非功能特性 —— 过长的日志消息
//     应由调用方自行拆分。
// =============================================================================

#pragma once
#include "Logger.h"   // LogLevel
#include <string>

namespace LogFormat {

// ---------------------------------------------------------------------------
// FormatLine — 生成一条完整的日志行（不含尾随 '\n'）
// ---------------------------------------------------------------------------
// 所有字符串参数均为以 null 结尾的 UTF-8。nullptr 被视为等同于
// ""（空字符串）。
//
// 线程安全:  纯函数。无共享状态。可从任意线程调用，无需同步。
//
// 内存:      按值返回 std::string（通常 ≤ 300 字节）。
//            移动语义使这对调用方来说成本很低。
std::string FormatLine(LogLevel level,
                       const char* module,
                       const char* operation,
                       const char* deviceId,
                       const char* caseId,
                       const char* operator_,
                       const char* content);

// ---------------------------------------------------------------------------
// LevelName — 人类可读的级别字符串（5 字符，右侧空格填充）
// ---------------------------------------------------------------------------
// 返回: "DEBUG", "INFO ", "WARN ", "ERROR", "FATAL"
// 返回的指针指向静态常量；绝不要释放它。
const char* LevelName(LogLevel level);

} // namespace LogFormat
