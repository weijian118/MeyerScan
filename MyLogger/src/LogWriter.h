// =============================================================================
// 文件:    LogWriter.h
// 模块:    MeyerScan_Logger.dll（内部）
//
// 用途:
//   持有当前打开的日志文件的 FILE* 句柄，并通过单个 Windows 命名互斥量
//   串行化所有磁盘写入，以便多个进程可以安全地追加到同一个日志文件。
//
// 多进程架构:
//   ┌─────────────────┐     ┌──────────────────────┐
//   │  MeyerScan.exe  │     │ ScanReconstructStudio │
//   │  （主进程）      │     │ （扫描进程）           │
//   │       │         │     │       │               │
//   │   LogWriter     │     │   LogWriter           │
//   │       │         │     │       │               │
//   │  WaitForSingle   │     │  WaitForSingle        │
//   │  Object(mutex)   │     │  Object(mutex)        │
//   │       │         │     │       │               │
//   └───────┼─────────┘     └───────┼───────────────┘
//           │                       │
//           └───────────┬───────────┘
//                       │ 由命名互斥量串行化
//                       ▼
//              MeyerScan_20260612_001.log
//
// 为什么使用命名互斥量而非 LockFileEx？
//   - 命名互斥量是内核对象；如果进程在持有它时崩溃，
//     操作系统会自动释放它（句柄被关闭）。不会死锁。
//   - LockFileEx 与文件句柄绑定；如果一个进程关闭其句柄
//     而另一个进程正在等待，行为是未定义的。
//   - 命名互斥量跨用户会话工作（使用 Global\ 前缀），
//     这在扫描进程以服务账户运行时很重要。
//
// 为什么使用 "Global\\" 前缀？
//   - 不使用此前缀，互斥量在会话命名空间中创建，
//     对其他会话中的进程不可见（例如 服务 vs 交互式用户）。
//   - 如果 SeCreateGlobalPrivilege 不可用（在锁定严格的
//     医院 PC 上很少见），我们回退到 "Local\\"，
//     至少在同一会话内串行化。
// =============================================================================

#pragma once
#include <cstdio>
#include <string>
#include <windows.h>

class LogWriter {
public:
    // 构造函数:  创建（或打开）命名互斥量。
    // 析构函数:  关闭文件句柄和互斥量句柄。
    LogWriter();
    ~LogWriter();

    // -------------------------------------------------------------------
    // Open — 创建/截断并打开日志文件以追加模式
    // -------------------------------------------------------------------
    // path:   绝对路径，例如 "C:\...\logs\MeyerScan_20260612_001.log"
    //         如果父目录不存在，将通过 SHCreateDirectoryExW 递归创建。
    //
    // 如果已有打开的文件，会先刷新并关闭它。
    //
    // 成功时返回 true。失败时，文件句柄保持为空，
    // 后续的 WriteLine() 调用为空操作（行被丢弃）。
    //
    // stdio 缓冲被禁用 (setvbuf _IONBF)。每次 WriteLine()
    // 直接进入操作系统写入缓存。这牺牲了一点吞吐量，
    // 但保证了在互斥量保护下的行级原子性。
    bool Open(const std::string& path);

    // -------------------------------------------------------------------
    // WriteLine — 向文件追加一行 + '\n'
    // -------------------------------------------------------------------
    // 以 500 ms 超时获取跨进程互斥量。
    //   - 成功时: 写入行、追加 '\n'、释放互斥量。
    //   - 超时时: 丢弃该行并调用 OutputDebugStringA，
    //     使失败在调试器中可见。
    //
    // 500 ms 超时是故意设置得很短。如果另一个进程卡住持有互斥量
    // 超过 500 ms，日志吞吐量已经是我们最不需要担心的问题 ——
    // 另一个进程很可能已挂起/崩溃。丢弃一行日志
    // 优于无限期阻塞调用线程。
    void WriteLine(const std::string& line);

    // -------------------------------------------------------------------
    // Flush — 刷新 FILE* 句柄的缓冲区
    // -------------------------------------------------------------------
    // 不获取跨进程互斥量；只保证本进程 CRT 缓冲的数据
    // 被提交给操作系统。
    void Flush();

    // -------------------------------------------------------------------
    // Close — 刷新并关闭
    // -------------------------------------------------------------------
    // 互斥量不会被释放（它的生命周期与 DLL 相同）。
    void Close();

    // -------------------------------------------------------------------
    // FileSize — 当前打开文件的大小，以字节为单位
    // -------------------------------------------------------------------
    // 对存储的路径使用 _stati64（无需打开第二个句柄）。
    // 如果没有打开的文件，返回 0。
    uint64_t FileSize() const;

private:
    FILE*       m_file  = nullptr;  // CRT 文件句柄（未打开时为 null）
    HANDLE      m_mutex = nullptr;  // 命名互斥量 "Global\MeyerScan_Logger_Mutex"
    std::string m_path;             // 当前打开文件的绝对路径
};
