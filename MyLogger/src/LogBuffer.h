// =============================================================================
// 文件:    LogBuffer.h
// 模块:    MeyerScan_Logger.dll（内部）
//
// 用途:
//   一个简单的、线程安全的格式化日志行累积器。
//
//   写入者（可能数十个应用线程）调用 Add() 来追加一行。
//   刷新线程（有且仅有一个）定期调用 Drain() 来原子地
//   接管所有待处理行并将它们写入磁盘。
//
// 设计理由:
//   - 对于我们的访问模式（多生产者、单一消费者、批量排空），
//     std::mutex + std::vector 比无锁队列更简单且开销更低。
//   - 互斥锁只被持有足够进行 push_back / swap 的时间；
//     格式化和磁盘 I/O 在临界区之外进行。
//   - kFlushThreshold（100 行）的选择使得在大量日志场景下，
//     磁盘最多每约 100 行被命中一次，同时保持足够低的延迟，
//     使崩溃前的最后几行不太可能丢失。
//     （Error 和 Fatal 行总是立即触发唤醒，不受阈值限制。）
// =============================================================================

#pragma once
#include <mutex>
#include <string>
#include <vector>

class LogBuffer {
public:
    // -------------------------------------------------------------------
    // Add — 追加一行预格式化的行（不含换行符）
    // -------------------------------------------------------------------
    // 实际上是非阻塞的；互斥锁仅被持有进行 vector 的 push_back
    //（约纳秒级）。
    //
    // 线程安全:  可从任意线程安全调用。
    void Add(const std::string& line);

    // -------------------------------------------------------------------
    // Drain — 原子地移除所有待处理行
    // -------------------------------------------------------------------
    // 调用方（刷新线程）获得所有已累积行的所有权。
    // Drain() 返回后，缓冲区为空。
    //
    // 线程安全:  可与 Add() 并发调用。
    //            只有一条线程应调用 Drain()（刷新线程）。
    std::vector<std::string> Drain();

    // -------------------------------------------------------------------
    // Size — 待处理行的大致数量
    // -------------------------------------------------------------------
    // 线程安全:  安全，但返回值在被调用方检查时可能已过时
    //            （互斥锁在函数返回前已被释放）。
    //            仅用于启发式决策（例如"我是否应唤醒刷新线程？"）。
    size_t Size() const;

    // -------------------------------------------------------------------
    // kFlushThreshold — 当 Size() 达到此值时唤醒刷新线程
    // -------------------------------------------------------------------
    // 选择 100 的理由:
    //   - 按每行 300 字节计算，100 行 ≈ 30 KB 内存 —— 微不足道。
    //   - 每 100 行刷新意味着在硬崩溃时最多约 100 行可能丢失
    //     （Error/Fatal 除外，它们会立即刷新）。
    //   - 配合 5 秒的后台定时器，100 行意味着在定时器成为主要刷新
    //     驱动因素之前，平均每秒 20 行。这对于医疗设备应用来说
    //     是一个健康的速率。
    static constexpr size_t kFlushThreshold = 100;

private:
    // mutable: Size() 在概念上是 const 的，但需要锁定互斥锁。
    mutable std::mutex m_mutex;

    // 缓冲区在两次 Drain() 调用之间无界增长。在最坏情况下
    //（Drain() 长时间未被调用），内存使用可能增长。
    // 后台线程的 5 秒超时为此设置了上限。
    std::vector<std::string> m_lines;
};
