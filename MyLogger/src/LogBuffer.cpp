// =============================================================================
// 文件:    LogBuffer.cpp
// 模块:    MeyerScan_Logger.dll（内部）
//
// 实现说明:
//   - 这是有意保持简单的。考虑过无锁 MPSC（多生产者、单一消费者）队列，
//     但因以下原因被否决:
//       a) 刷新线程批量排空（全有或全无的 swap），
//          std::vector + std::mutex 对此是最优的。
//       b) 无锁队列增加显著的复杂性（ABA 问题、内存顺序、
//          缓存行填充），而在日志路径中磁盘 I/O 才是瓶颈，
//          收益微不足道。
//       c) 在稳态下互斥锁是无竞争的（刷新线程大部分时间在休眠）。
//   - Drain() 使用 vector::swap() 在 O(1) 时间内将行移出，
//     无需分配内存。调用方 (DoFlush) 随后在缓冲区互斥锁已释放的
//     情况下遍历并写入每一行到磁盘。
//   - Size() 是一个无锁的提示。返回值可能在一条指令之后就已过时，
//     但调用方 (Write()) 只用它来决定是否通知条件变量 ——
//     假阳性导致一次虚假唤醒（无害），假阴性意味着刷新在
//     下一个定时器周期而不是立即发生（也无害）。
// =============================================================================

#include "LogBuffer.h"

// ---------------------------------------------------------------------------
// Add
// ---------------------------------------------------------------------------
// 复杂度:  O(1) 摊销。互斥锁被持有用于一次 vector::push_back，
// 在常见情况下这只有少量 CPU 指令（无需重新分配 ——
// vector 很快增长到其稳态大小）。
void LogBuffer::Add(const std::string& line) {
    std::lock_guard<std::mutex> lock(m_mutex);
    m_lines.push_back(line);
}

// ---------------------------------------------------------------------------
// Drain
// ---------------------------------------------------------------------------
// 复杂度:  O(1)。vector::swap 交换三个指针；在临界区内没有
// 元素复制或析构发生。
//
// 调用方收到 vector 的副本（从交换后的临时对象移动构造）。
// Drain() 返回后，内部 vector 为空，调用方拥有所有行。
std::vector<std::string> LogBuffer::Drain() {
    std::vector<std::string> drained;
    {
        std::lock_guard<std::mutex> lock(m_mutex);
        // 交换内容:  m_lines 变为空，drained 拥有数据。
        drained.swap(m_lines);
    }
    // 互斥锁在此处释放 — 调用方可以进行缓慢的 I/O
    // 而不会阻塞任何生产者线程。
    return drained;
}

// ---------------------------------------------------------------------------
// Size
// ---------------------------------------------------------------------------
size_t LogBuffer::Size() const {
    std::lock_guard<std::mutex> lock(m_mutex);
    return m_lines.size();
}
